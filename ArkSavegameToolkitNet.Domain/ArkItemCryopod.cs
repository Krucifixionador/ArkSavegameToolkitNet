using ArkSavegameToolkitNet.DataTypes;
using ArkSavegameToolkitNet.DataTypes.Properties;
using ArkSavegameToolkitNet.Domain.Utils.Extensions;
using ArkSavegameToolkitNet.Utils.Extensions;
using System.Linq;

namespace ArkSavegameToolkitNet.Domain
{
    public class ArkItemCryopod : ArkItem
    {
        internal static readonly ArkName _customItemDatas = ArkName.Create("CustomItemDatas");
        internal static readonly ArkName _customDataName = ArkName.Create("CustomDataName");
        internal static readonly ArkName _customDataBytes = ArkName.Create("CustomDataBytes");
        internal static readonly ArkName _byteArrays = ArkName.Create("ByteArrays");
        internal static readonly ArkName _bytes = ArkName.Create("Bytes");

        internal static readonly new ArkNameTree _dependencies = new ArkNameTree
        {
            {
                _customItemDatas,
                new ArkNameTree
                {
                    {
                        _customDataBytes,
                        new ArkNameTree
                        {
                            {
                                _byteArrays,
                                new ArkNameTree
                                {
                                    { _bytes, null }
                                }
                            }
                        }
                    },
                    { _customDataName, null }
                }
            }
        };

        public ArkItemCryopod() : base()
        {
        }

        public ArkItemCryopod(GameObject item, ArkSaveData saveState) : base(item, saveState)
        {
            // load dinos that are stored in items such as the PrimalItem_WeaponEmptyCryopod_C
            var customItemDatas = item.GetProperty<StructArrayProperty>(_customItemDatas);
            if (customItemDatas != null)
            {
                var dinos = customItemDatas.values.OfType<PropertyListStructProperty>().Where(x =>
                    x.GetPropertyValue<ArkName>(_customDataName)?.Token?.Equals("Dino") == true
                    && x.Properties.ContainsKey(_customDataBytes)).ToArray();

                if (dinos.Length == 0) return;

                var dinoDatas = dinos.Select(x =>
                {
                    var customDataBytes = x.GetProperty<PropertyListStructProperty>(_customDataBytes);
                    var byteArrays = customDataBytes?.GetProperty<StructArrayProperty>(_byteArrays);
                    var data = byteArrays?.values.OfType<PropertyListStructProperty>().FirstOrDefault()?.GetProperty<ByteArrayProperty>(_bytes);

                    if (data == null) return null;

                    return ArkToolkit.LoadFrozenDino(data.values.ToArray());
                }).Where(x => x != null).ToArray();

                var objects = dinoDatas?.FirstOrDefault().objects;
                var dino = objects.ElementAtOrDefault(0);
                var status = objects.ElementAtOrDefault(1);
                var inventory = objects.ElementAtOrDefault(2);

                Dino = dinoDatas?.FirstOrDefault().objects.FirstOrDefault().AsTamedCreature(status, inventory, saveState);
            }
        }

        public ArkTamedCreature Dino { get; set; }
    }
}

